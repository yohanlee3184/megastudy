#include <stdio.h>

int main_02(void)
{

	int num1 = 20; // 0001 0100
	int num2 = 16; // 0001 0000
	int num3 = 2147483647; // 0111 1111 1111 1111 1111 1111 1111 1111

	int r1, r2, r3, r4, r5;

	r1 = num1 << 1;
	printf("%d의 비트 왼쪽 1 시프트 연산의 결과 %d\n", num1, r1);

	r2 = num1 >> 1;
	printf("%d의 비트 오른쪽 1 시프트 연산의 결과 %d", num1, r2);

	r3 = num3 << 1;
	printf("%d의 비트 연산 왼쪽 1 시프트 연산의 결과 %d.\n", num3, r3);
	return 0;
}

/* 최상위 비트가 1이면 음수임
1 000 0001 +
1 000 0011 +
1 000 0100 +

십진수 1

0000 0001 -> 1111 1110 -> +1 -> 1111 1111 = -1 (2의 보수법)
0000 0001 -> (~) 1111 1110 -> +1 -> 1111 1111 = -1

십진수 2 ->

0000 0010 -> (~) 1111 1101 -> +1 -> 1111 1110 = -2

1) 십진수를 2진수로 변환하고
2) 보수 (Not)
3) +1

3 -1 = 3 + (-1)

0000 0011 = 3
1111 1111 = -1

0000 0001 -> 1111 1110 -> +1 -> 1111 1111

*/

